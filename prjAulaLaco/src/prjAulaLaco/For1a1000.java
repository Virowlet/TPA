package prjAulaLaco;

public class For1a1000 {

	public static void main(String args[]) {
		
		int i;
		
		for(i=1;i<=1000;i++) {
			
			if(i%2!=0) {
				System.out.print(i);
			}
			else {
				System.out.print(" ");
			}
			
		}//fim do for
	}//fim do metodo main
}//fim da class
