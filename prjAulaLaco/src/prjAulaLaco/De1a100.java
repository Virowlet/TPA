package prjAulaLaco;

public class De1a100 {

	public static void main(String[] args) {
		
		int i,s;
		
		i=1;
		s=0;
		
		while(i<=100) {
			s = s+i;
			i++;
		}//fim do while
		System.out.println("A somatória dos números de 1 a 100 é = " + s);
	}// fim do metodo main
}//fim da class
